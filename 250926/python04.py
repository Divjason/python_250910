print("%s %s" % ("one", "two")) # 뒤에 입력된 튜플자료구조의 순서에 따라서 출력
print("{} {}".format("one", "two"))
print("{0} {1}".format("one", "two"))
print("{1} {0}".format("one", "two"))

print("%10s" % ("nice"))
print("{:>10}".format("nice"))

print("%-10s" % ("nice"))
print("{:<10}".format("nice"))

print("{:^10}".format("nice"))

print("%.4s" % ("smart"))
print("%4s" % ("smart"))
print("%8s" % ("smart"))

print("{:10.5}".format("pythonstudy"))

print("%d %d" % (1, 2))
print("{} {}".format(1, 2))
print("{1} {0}".format(1, 2))
print("%4d" % (42))

print("%f" % (3.1434343434))
print("%1.4f" % (3.1434343434))